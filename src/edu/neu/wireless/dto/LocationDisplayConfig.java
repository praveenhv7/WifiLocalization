package edu.neu.wireless.dto;

public class LocationDisplayConfig {
	
	private int x;
	private int y;
	private int SCALE_WIDTH;
	private int SCALE_HEIGHT;
	
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getSCALE_WIDTH() {
		return SCALE_WIDTH;
	}
	public void setSCALE_WIDTH(int sCALE_WIDTH) {
		SCALE_WIDTH = sCALE_WIDTH;
	}
	public int getSCALE_HEIGHT() {
		return SCALE_HEIGHT;
	}
	public void setSCALE_HEIGHT(int sCALE_HEIGHT) {
		SCALE_HEIGHT = sCALE_HEIGHT;
	}

	
}
